# OPERATORS
# ARITHMETIC OPERATORS
sumOperator = 1 + 2
multiplyOperator = 1 * 2
divideOperator = 1 / 2
moduloOperator = 1 % 2
powerOperator = 2 ** 3
print(sumOperator)
print(multiplyOperator)
print(divideOperator)
print(moduloOperator)
print(powerOperator)

# COMPARISON OPERATORS
print(10 > 5)  # GREATER THAN
print(10 >= 5)  # GREATER THAN OR EQUAL TO
print(10 < 5)  # LESS THAN OR EQUAL TO
print(10 <= 5)  # LESS THAN OR EQUAL TO
print(10 == 5)  # EQUAL TO
print(10 != 5)  # NOT EQUAL TO

# LOGICAL OPERATORS
print((10 > 5) and (1 < 3) and "A" == "A")  # AND
print((10 > 5) or (1 > 3))  # OR
print(not (10 > 5))  # NOT

# ASSIGNMENT OPERATORS
brand = "Some brand here"
brand = "Overrides latest value"
print(brand)
number = 0
number += 1  # number = number + 1 -- ANY ARITHMETIC OPERATOR
print(number)
